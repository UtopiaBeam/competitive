#include<cstdio>
char a[10][30]={"+------------------------+","|#.#.#.#.#.#.#.#.#.#.#.|D|)","|#.#.#.#.#.#.#.#.#.#.#.|.|","|#.......................|","|#.#.#.#.#.#.#.#.#.#.#.|.|)","+------------------------+"};
int main(){
    int n;
    scanf("%d",&n);
    for(int i=1;i<=n;i++){
        if(i<5)     a[i][1]='O';
        else if(i%3==1)     a[4][2*((i-1)/3)-1]='O';
        else        a[(i-1)%3][2*((i-1)/3)+1]='O';
    }
    for(int i=0;i<6;i++)
        printf("%s\n",a[i]);
    return 0;
}
